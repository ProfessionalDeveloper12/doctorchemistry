class Ocodewire_BitcoinPayment_Model_BitcoinPayment extends Mage_Payment_Model_Method_Abstract
{
    /**
    * unique internal payment method identifier
    *
    * @var string [a-z0-9_]
    */
    protected $_code = 'bitcoinpayment';

    protected $_formBlockType = 'bitcoinpayment/form';
    protected $_infoBlockType = 'bitcoinpayment/info';
    protected $_accounts;

 

    public function getCustomText($addNl2Br = true)
    {
        $customText = $this->getConfigData('customtext');
        if ($addNl2Br) {
            $customText = nl2br($customText);
        }
        return $customText;
    }

}
